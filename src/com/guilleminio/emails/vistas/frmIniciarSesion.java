    private void iniciarSesion(){
        if ( formularioCompleto()){

            String email = this.jTxEmail.getText();
            String contrasenia = String.valueOf(this.jPtxtContrasenia.getPassword());

            if ( this.controladora.iniciarSesion(email, contrasenia)){
                frmPrincipal vPrincipal = new frmPrincipal(this.controladora.obtenerIdUsuario());
                this.setVisible(false);
                vPrincipal.setVisible(true);
                this.dispose();
            }else{
                this.jLErrorDatosIncorrectos.setVisible(true);
            }
       }
    }
    
