    @Override
    public void inicializarVentana() {
        this.setLocationRelativeTo(this);
        this.controladora = new IniciarSesionControladora();
        limpiarErrores();
        
    }

    @Override
    public boolean formularioCompleto() {
    
        limpiarErrores();
        
        String email = this.jTxEmail.getText();
       
        if ( email.equals("")){
            this.jLErrorEmail.setVisible(true);
            return false;
        }
        
        if ( !Utilidades.validarFormatoEmail(email) ){
            this.jLErrorEmail.setText("El formato del email no es correcto.");
            this.jLErrorEmail.setVisible(true);
            return false;
        }
        
        if ( this.jPtxtContrasenia.getPassword().length == 0){
            this.jLErrorContrasenia.setVisible(true);
            return false;
        }
        return true;
    }

    @Override
    public void abrirVentana() {
    
        frmRegistroPaso1 vRegistro = new frmRegistroPaso1();
        this.setVisible(false);
        vRegistro.setVisible(true);
        this.dispose();
    }

    @Override
    public void limpiarErrores() {
        this.jLErrorEmail.setText("Debes indicar tu email.");
        this.jLErrorEmail.setVisible(false);
        this.jLErrorContrasenia.setVisible(false);
        this.jLErrorDatosIncorrectos.setVisible(false);
    }
