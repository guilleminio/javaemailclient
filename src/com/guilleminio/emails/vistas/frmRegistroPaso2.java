    private void jBtnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnAgregarActionPerformed
        // TODO add your handling code here:
        
        FileNameExtensionFilter imageFilter = new FileNameExtensionFilter("JPG","jpg");
        
        jImgFileChooser = new JFileChooser("res\\imagenes_usuarios\\para_agregar");
        jImgFileChooser.setDialogTitle("Elige una imagen");
        jImgFileChooser.setFileFilter(imageFilter);
        
        if ( jImgFileChooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION){
        
                File archivoImagen = jImgFileChooser.getSelectedFile();

            try{

                ImageIcon icon = new ImageIcon(archivoImagen.toString());
                System.out.println(archivoImagen.toString());
                Icon icono = new ImageIcon(icon.getImage().getScaledInstance(this.jLImagen.getPreferredSize().width, this.jLImagen.getPreferredSize().height, Image.SCALE_DEFAULT));

                this.jLImagen.setIcon( icono );

            }catch(Exception ex){
                JOptionPane.showMessageDialog(null, "Error abriendo la imagen "+ ex);     
            }
        }
    }//GEN-LAST:event_jBtnAgregarActionPerformed

    private void jBtnContinuarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnContinuarActionPerformed
        // TODO add your handling code here:
        
        if ( this.imagenPorDefecto.equals(this.jLImagen.getIcon())){
            
            int opcionElegida = JOptionPane.showConfirmDialog(this,"¿Seguro no deseas agregar una imagen?","Atención!",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
        
            if ( opcionElegida == JOptionPane.YES_OPTION){
                
                int opcion = JOptionPane.showConfirmDialog(this, "Ya eres miembr@ de myemai.com","¡GENIAL!",JOptionPane.DEFAULT_OPTION,JOptionPane.INFORMATION_MESSAGE);
            
                if ( opcion == JOptionPane.YES_OPTION ){
                    
                    abrirVentana();
                }
            }
        }
        else{
            String rutaImagenUsuario = generarImagenUsuario();
            
            if ( this.controladora.agregarImagenUsuario(this.idUsuario, rutaImagenUsuario)){
            
                int opcion = JOptionPane.showConfirmDialog(this, "Ya eres miembr@ de myemai.com","¡GENIAL!",JOptionPane.DEFAULT_OPTION,JOptionPane.INFORMATION_MESSAGE);
            
                if ( opcion == JOptionPane.YES_OPTION ){
                    
                    abrirVentana();
                }
            }
        
        }
       
    }//GEN-LAST:event_jBtnContinuarActionPerformed

