    private void cargarImagenDefault(){
        
        ImageIcon imagen_perfil = new ImageIcon("res\\imagenes_usuarios\\user_default.jpg");
        
        this.imagenPorDefecto = new ImageIcon(imagen_perfil.getImage().getScaledInstance(this.jLImagen.getPreferredSize().width, this.jLImagen.getPreferredSize().width, Image.SCALE_DEFAULT));
        this.jLImagen.setText("");
        this.jLImagen.setIcon(this.imagenPorDefecto);
    }
    
    private String guardarImagenUsuario( File aImage ){
        
        String res = null;
        
        try{
            this.streamOut = new FileOutputStream( aImage );
            this.streamOut.write(this.imagenGuardar);
            res = "Foto guardada";
        }catch (Exception e){
            System.out.println(e.toString());
        }
        
        return res;
    }
    
    
    private String generarImagenUsuario(){
        
        String urlPath = null;
        
        if ( this.jImgFileChooser != null ){
            
            File imagen = this.jImgFileChooser.getSelectedFile();
            
            try
            {
                BufferedImage originalImage = ImageIO.read(imagen);
            
                urlPath = Utilidades.DEFAULT_PATH_USER_PROFILE + this.idUsuario + Utilidades.DEFAULT_IMAGE_EXTENSION;
            
                ImageIO.write(originalImage, "jpg", new File(urlPath));
                return urlPath;
            } 
            catch (IOException e ) 
            {
                e.printStackTrace();
            }
           
       }
       
       return null;
    }
    
